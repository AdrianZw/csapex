//// class LogAdapter //////////////////////////////////////////////////////////

LogAdapter::LogAdapter() {
    mLogCollector = NULL;
}

void LogAdapter::updateLogValues() {
    if ( mLogCollector != NULL ) {
        writeLogData( mLogCollector );
    }
}

void LogAdapter::enableLogging( LogCollector * logCollector, bool trigger ) {
    mLogCollector = logCollector;
    addLogColumns( logCollector, trigger );
}


